// Tarpusavyje palyginti skaičiaus tipo kintamuosius:
const num1 = 5;
const num2 = 12;

if (num1 > num2) {
    console.log('True');
} else {
    console.log(`${num1} > ${num2}`);
    console.log('False');
};

if (num1 < num2) {
    console.log(`${num1} < ${num2}`);
    console.log('True');
} else {
    console.log('False');
};

if (num1 === num2) {
    console.log(`${num1} === ${num2}`);
    console.log('True');
} else {
    console.log(`${num1} === ${num2}`);
    console.log('False');
};

if (num1 !== num2) {
    console.log(`${num1} !== ${num2}`);
    console.log('True');
} else {
    console.log(`${num1} !== ${num2}`);
    console.log('False');
};

if (num1 >= num2) {
    console.log(`${num1} >= ${num2}`);
    console.log('True');
} else {
    console.log(`${num1} >= ${num2}`);
    console.log('False');
};

if (num1 <= num2) {
    console.log(`${num1} <= ${num2}`);
    console.log('True');
} else {
    console.log(`${num1} <= ${num2}`);
    console.log('False');
};

